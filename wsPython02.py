#โจทย์ข้อที่ 2

#รับค่าความกว้าง,ยาว,ราคา,(ต่อเมตร)
x = float(input("ความกว้าง (เมตร): "))
y = float(input("ความยาว (เมตร): "))
z = float(input("ราคาต่อเมตร (บาท): "))

#คำนวณราคาค่าจ้างตัดหญ้า
total = x * y * z

#แสดงผลลัพธ์ โดยให้แสดงผลเป็นทศนิยม 2 ตำแหน่ง
print("ราคาค่าจ้างตัดหญ้า: %.2f บาท" % total)


